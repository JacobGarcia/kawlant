function credentials(state = {}, action) {
  switch (action.type) {
    case 'SET_USER':
      return {
        user: action.user,
        token: action.token,
        authenticated: true
      }
    case 'LOG_OUT':
      return {}
    default:
      return state
  }
}

function zones(state = dumbZones, action) {
  switch (action.type) {
    case 'SET_ZONE':
    return [...state, {
      name: action.name,
      positions: action.positions
    }]
    case 'SET_SUBZONE':
    return [...state]
    default:
    return state
  }
}

function administrators(state = dumbAdministrators, action) {
  switch (action.type) {
    default:
      return state
  }
}

function reports(state = dumbReports, action) {
  switch (action.type) {
    case 'SET_REPORTS':
      return [
        ...action.reports
      ]
    case 'DISMISS_REPORT':
      return state.map(report =>
        (report._id === action.report)
          ? {...report, dismissed: true}
          : report
      )
    default:
      return state
  }
}

const appReducer = combineReducers({
  credentials,
  zones,
  reports,
  administrators
})

export default appReducer
